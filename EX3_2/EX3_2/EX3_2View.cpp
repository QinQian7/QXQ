// CEX3_2View 消息处理程序


void CEX3_2View::OnLButtonDown(UINT nFlags, CPoint point)
{
	// TODO: 在此添加消息处理程序代码和/或调用默认值
	int a = rand() % 50 + 10;
	int b = rand() % 50 + 30;
	if (a != b || a<b)
	{
		CRect rc(point.x - b, point.y - a, point.x + b, point.y + a);
		ca.Add(rc);
	}
	this->Invalidate();

	CView::OnLButtonDown(nFlags, point);
}
